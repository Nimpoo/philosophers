#include "../philo.h"

int	ft_atoi(const char *str)
{
	int		i;
	long	j;
	long	n;

	i = 0;
	j = 1;
	n = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
			i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
				j = j * -1;
		i++;
		if (str[i] == '+' || str[i] == '-')
			return (0);
	}
	while (str[i] >= '0' && str [i] <= '9')
	{
			n = n * 10 + (str[i] - '0');
			i++;
	}
	return (n * j);
}
